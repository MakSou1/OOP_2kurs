public class Main
{
    public static void main (String[] args) {

        int a = 0;
        int b = 2;
        int c = 8;
        int d = 1;
        int e = 5;
        int f = 7;
        int g = 4;
        int h = 3;
        int i = 6;
        int j = 9;

        int sum = a + b + c + d + e + f + g + h + i + j;
        System.out.println("Сума всіх змінних = " + sum);

        int diff = a - b - c - d - e - f - g - h - i - j;
        System.out.println("Різниця всіх змінних від першої = " + diff);

        int prod = a * b * c * d * e * f * g * h * i * j;
        System.out.println("Добуток всіх змінних = " + prod);

        int quot = a / b / c / d / e / f / g / h / i / j;
        System.out.println("Частка першої змінної на всі інші = " + quot);
    }
}